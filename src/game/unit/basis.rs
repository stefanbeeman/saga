pub enum UnitBasis {
    Animal, // unit is a mobile living thing, but has non-humanoid body configuration.
    Human, // unit is basically bipedal, can carry things, and use tools.
    Sprit, // unit is a supernatural being that transcends material form.
    Plant, // unit is alive, but lacks centralized organs and senses.
    Thing, // unit is a physical object animated by supernatural forces.
}
